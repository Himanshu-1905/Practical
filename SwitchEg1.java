
public class SwitchEg1 {
	public static void main(String[] args) { //main=ctrl+space
		char ch='h';
		switch (ch) {
		case 'a':
			System.out.println("First case execute");
			break;
		case 'b':
			System.out.println("Second case execute");
			break;
		case 'c':
			System.out.println("Second case execute");
			break;
		case 'h':
			System.out.println("Last case execute");
			break;
			default:System.out.println("Invalid");
			
		}
			
	}

}
